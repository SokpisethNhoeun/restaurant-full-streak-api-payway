export function LanguageProvider({ children }) {
  const [language, setLanguageState] = useState("en");

  useEffect(() => {
    const stored = window.localStorage.getItem(STORAGE_KEY);
    if (stored === "km" || stored === "en") {
      setLanguageState(stored);
    }
  }, []);

  const setLanguage = useCallback((nextLanguage) => {
    setLanguageState(nextLanguage);
    window.localStorage.setItem(STORAGE_KEY, nextLanguage);
    document.documentElement.lang = nextLanguage === "km" ? "km" : "en";
  }, []);

  const toggleLanguage = useCallback(() => {
    setLanguage(language === "km" ? "en" : "km");
  }, [language, setLanguage]);

  const value = useMemo(() => ({
    language,
    setLanguage,
    toggleLanguage,
    t: (key) => DICTIONARY[language]?.[key] || DICTIONARY.en[key] || key
  }), [language, setLanguage, toggleLanguage]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  return useContext(LanguageContext);
}

export function LanguageToggle({ className }) {
  const { language, setLanguage, t } = useLanguage();
  const currentLanguage = LANGUAGE_OPTIONS.find((option) => option.value === language) || LANGUAGE_OPTIONS[0];

  return (
    <DropdownMenu>
      <DropdownMenuTrigger asChild>
        <Button
          type="button"
          variant="outline"
          className={cn("min-h-10 rounded-xl px-3 text-xs", className)}
          aria-label={t("changeLanguage")}
        >
          <Languages className="h-4 w-4" />
          <span className="font-bold leading-none">{currentLanguage.label}</span>
          <ChevronDown className="h-3.5 w-3.5 text-muted-foreground" />
        </Button>
      </DropdownMenuTrigger>
      <DropdownMenuContent align="end">
        {LANGUAGE_OPTIONS.map((option) => (
          <DropdownMenuItem key={option.value} onSelect={() => setLanguage(option.value)}>
            <span className="flex h-4 w-4 items-center justify-center">
              {language === option.value ? <Check className="h-4 w-4" /> : null}
            </span>
            <span className="font-medium">{option.label}</span>
          </DropdownMenuItem>
        ))}
      </DropdownMenuContent>
    </DropdownMenu>
  );
}
